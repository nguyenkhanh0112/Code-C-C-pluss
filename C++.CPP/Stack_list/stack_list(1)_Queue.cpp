/*================== STACK-NGĂN XẾP ===================
STACK là 1 cấu trúc trừu tượng, 
các đối tượng trong stack phải làm việc theo 1 LIFO(LAST IN FIRST OUT)
-đối tượng nào vào sau thì sẽ ra trước*/

/*=================== QUEUE- HÀNG ĐỢI ============
Queue là 1 cấu trúc trừu tượng, các đối tượng trong Queue phải làm việc 
theo 1 cơ chế FIFO(FIRST IN FIRST OUT) - đối tượng nào vào trước thì sẽ ra trước */